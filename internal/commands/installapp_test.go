func testDeps(t *testing.T, goos string) (installDeps, *[]string) {
	t.Helper()
	var calls []string
	return installDeps{
		goos: goos,
		home: t.TempDir(),
		exe:  "/fake/bin/byre",
		run: func(name string, args ...string) error {
			calls = append(calls, name+" "+strings.Join(args, " "))
			if name == "osacompile" {
				// The real tool creates the bundle; the fake mimics the
				// Resources layout the installer writes into.
				app := args[1]
				if err := osMkdirAll(app+"/Contents/Resources", 0o755); err != nil {
					return err
				}
			}
			return nil
		},
	}, &calls
}

func TestInstallDarwin(t *testing.T) {
	d, calls := testDeps(t, "darwin")
	s, out, errw := testStreams("", false)
	if err := installApp(s, "proj-x", d); err != nil {
		t.Fatal(err)
	}
	app := d.home + "/Applications/Byre Deliver.app"
	src, err := osReadFile(app + "/Contents/Resources/droplet.applescript")
	if err != nil || !strings.Contains(string(src), "Generated by byre") || !strings.Contains(string(src), "--box 'proj-x'") {
		t.Fatalf("bundle source wrong: %v %q", err, src)
	}
	if icns, err := osReadFile(app + "/Contents/Resources/droplet.icns"); err != nil || string(icns[:4]) != "icns" {
		t.Fatalf("icon not installed: %v", err)
	}
	joined := strings.Join(*calls, "\n")
	staged := d.home + "/Applications/.Byre Deliver.staged.app"
	if !strings.Contains(joined, "osacompile -o "+staged) ||
		!strings.Contains(joined, "codesign --force --deep --sign - "+staged) ||
		!strings.Contains(joined, "touch "+app) {
		t.Fatalf("tool calls wrong (assemble+sign staged, swap, touch final):\n%s", joined)
	}
	if wf, err := osReadFile(d.home + "/Library/Services/Deliver to Byre.workflow/Contents/document.wflow"); err != nil || !strings.Contains(string(wf), "byre") {
		t.Fatalf("quick action missing: %v", err)
	}
	// stdout carries exactly the installed paths.
	if lines := strings.Split(strings.TrimSpace(out.String()), "\n"); len(lines) != 2 ||
		!strings.HasSuffix(lines[0], "Byre Deliver.app") || !strings.HasSuffix(lines[1], ".workflow") {
		t.Fatalf("stdout = %q", out.String())
	}
	if !strings.Contains(errw.String(), "to uninstall") {
		t.Fatalf("no uninstall story: %q", errw.String())
	}
}

func TestInstallDarwinRegeneratesOwnRefusesForeign(t *testing.T) {
	d, _ := testDeps(t, "darwin")
	s, _, _ := testStreams("", false)
	if err := installApp(s, "", d); err != nil {
		t.Fatal(err)
	}
	// Second run over our own artifact: fine (regeneration).
	if err := installApp(s, "other-box", d); err != nil {
		t.Fatalf("regeneration refused: %v", err)
	}
	// A same-named app byre did NOT write: refused, not clobbered.
	marker := d.home + "/Applications/Byre Deliver.app/Contents/Resources/droplet.applescript"
	if err := osWriteFile(marker, []byte("something else entirely"), 0o644); err != nil {
		t.Fatal(err)
	}
	if err := installApp(s, "", d); err == nil || !strings.Contains(err.Error(), "doesn't look byre-generated") {
		t.Fatalf("foreign app clobbered: err = %v", err)
	}
}

