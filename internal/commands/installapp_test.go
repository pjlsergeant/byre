func TestInstallDarwinOsacompileFailurePreservesOldApp(t *testing.T) {
	// Review finding: regeneration must never destroy the working app when
	// assembly fails — compile staged, swap last.
	d, _ := testDeps(t, "darwin")
	s, _, _ := testStreams("", false)
	if err := installApp(s, "", d); err != nil {
		t.Fatal(err)
	}
	realRun := d.run
	d.run = func(name string, args ...string) error {
		if name == "osacompile" {
			return errFake
		}
		return realRun(name, args...)
	}
	if err := installApp(s, "", d); err == nil {
		t.Fatal("expected the osacompile failure to surface")
	}
	marker := d.home + "/Applications/Byre Deliver.app/Contents/Resources/droplet.applescript"
	if src, err := osReadFile(marker); err != nil || !strings.Contains(string(src), "Generated by byre") {
		t.Fatalf("previous app was destroyed by a failed regeneration: %v", err)
	}
}

func TestInstallDarwinRefusesForeignQuickAction(t *testing.T) {
	// Review finding: the Quick Action check must require the explicit
	// marker — a wflow merely containing 'byre', or an unreadable bundle,
	// is refused.
	d, _ := testDeps(t, "darwin")
	s, _, _ := testStreams("", false)
	svc := d.home + "/Library/Services/Deliver to Byre.workflow"
	if err := osMkdirAll(svc+"/Contents", 0o755); err != nil {
		t.Fatal(err)
	}
	if err := osWriteFile(svc+"/Contents/document.wflow", []byte("someone's workflow that mentions byre"), 0o644); err != nil {
		t.Fatal(err)
	}
	if err := installApp(s, "", d); err == nil || !strings.Contains(err.Error(), "doesn't look byre-generated") {
		t.Fatalf("foreign quick action not refused: %v", err)
	}
	// The refusal preflights BEFORE the app half runs: nothing may have
	// been installed or announced (external review: half-committed installs
	// make the error unreliable and the retry misleading).
	if _, err := os.Stat(d.home + "/Applications/Byre Deliver.app"); !os.IsNotExist(err) {
		t.Fatalf("foreign quick action refusal must leave the app uninstalled: %v", err)
	}
	// Bundle present but wflow missing entirely: also refused.
	if err := osWriteFile(svc+"/Contents/document.wflow", nil, 0o644); err != nil {
		t.Fatal(err)
	}
	if err := installApp(s, "", d); err == nil {
		t.Fatal("marker-less bundle not refused")
	}
}

