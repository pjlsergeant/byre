// TestDockerfileGolden pins the exact bytes so layer ordering and formatting
// can't drift silently — cross-project layer caching depends on byte stability.
func TestDockerfileGolden(t *testing.T) {
	const want = `# Generated by byre. Do not edit — change byre.config and re-run.
FROM node:22

# --- template block ---

# --- byre infra layer (constant) ---
RUN apt-get update \
 && apt-get install -y --no-install-recommends gosu \
 && rm -rf /var/lib/apt/lists/*
RUN useradd --create-home --home-dir /home/dev --shell /bin/bash dev || true
ENV PATH=/home/dev/.local/bin:$PATH
COPY byre-launch /usr/local/bin/byre-launch
RUN chmod +x /usr/local/bin/byre-launch && mkdir -p /workspace && chown dev:dev /workspace /home/dev

# --- skills ---

# --- project block ---
WORKDIR /workspace

ENTRYPOINT ["/usr/local/bin/byre-launch"]
`
	if got := Dockerfile(Input{Base: "node:22"}); got != want {
		t.Fatalf("golden mismatch:\n--- got ---\n%s\n--- want ---\n%s", got, want)
	}
}

