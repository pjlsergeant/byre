// TestDockerfileGolden pins the exact bytes so layer ordering and formatting
// can't drift silently — cross-project layer caching depends on byte stability.
func TestDockerfileGolden(t *testing.T) {
	const want = `# Generated by byre. Do not edit — change byre.config and re-run.
FROM node:22

# --- template block ---

# --- byre core block (constant) ---
ARG BYRE_UID=1000
ARG BYRE_GID=1000
RUN apt-get update \
 && apt-get install -y --no-install-recommends gosu \
 && rm -rf /var/lib/apt/lists/*
RUN if getent passwd dev >/dev/null 2>&1; then sed -i '/^dev:/d' /etc/passwd; fi \
 && if getent group dev >/dev/null 2>&1; then sed -i '/^dev:/d' /etc/group; fi \
 && if ! getent group "$BYRE_GID" >/dev/null 2>&1; then echo "dev:x:${BYRE_GID}:" >> /etc/group; fi \
 && echo "dev:x:${BYRE_UID}:${BYRE_GID}:byre:/home/dev:/bin/bash" >> /etc/passwd \
 && mkdir -p /home/dev /workspace /inbox && chown "${BYRE_UID}:${BYRE_GID}" /home/dev /inbox
ENV PATH=/home/dev/.local/bin:$PATH
COPY byre-launch /usr/local/bin/byre-launch
RUN chmod +x /usr/local/bin/byre-launch
COPY byre-profile-env.sh /etc/profile.d/byre-env.sh

# --- skills ---

# --- mcp (canonical declared set; stable path) ---
COPY mcp.json /etc/byre/mcp.json

# --- claude skills (canonical declared set; stable path) ---
COPY claude-skills /etc/byre/claude-skills

# --- project block ---
WORKDIR /workspace

# --- image PATH capture (login shells restore from this; see byre-env.sh) ---
RUN mkdir -p /etc/byre && printf '%s\n' "$PATH" > /etc/byre/image-path

HEALTHCHECK NONE
USER dev
ENTRYPOINT ["/usr/local/bin/byre-launch"]
`
	if got := Dockerfile(Input{Base: "node:22"}); got != want {
		t.Fatalf("golden mismatch:\n--- got ---\n%s\n--- want ---\n%s", got, want)
	}
}

