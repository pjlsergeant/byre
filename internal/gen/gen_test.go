func TestDockerfileVolumeDirsDevOwned(t *testing.T) {
	out := Dockerfile(Input{Base: "node:22", VolumeDirs: []string{
		"/home/dev/.claude", "/workspace/node_modules", "/home/dev/.claude", // dup
	}})
	// One RUN, sorted + deduped, mkdir + chown dev:dev.
	want := "RUN mkdir -p '/home/dev/.claude' '/workspace/node_modules' && chown dev:dev '/home/dev/.claude' '/workspace/node_modules'\n"
	if !strings.Contains(out, want) {
		t.Fatalf("volume-dirs RUN missing/wrong:\n%s", out)
	}
}

func TestDockerfileNoVolumeDirsSection(t *testing.T) {
	out := Dockerfile(Input{Base: "node:22"})
	if strings.Contains(out, "volume mount points") {
		t.Fatalf("empty VolumeDirs should emit no section:\n%s", out)
	}
}

func TestDockerfileVolumeDirInjectionQuoted(t *testing.T) {
	// A hostile target must not break out of the RUN shell command.
	out := Dockerfile(Input{Base: "node:22", VolumeDirs: []string{"/x'; rm -rf /; echo '"}})
	if strings.Contains(out, "rm -rf /;") && !strings.Contains(out, `'\''`) {
		t.Fatalf("volume target not safely quoted:\n%s", out)
	}
}
