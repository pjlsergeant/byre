func TestDockerfileCanonicalOrder(t *testing.T) {
	out := Dockerfile(Input{Base: "node:22", Skills: []SkillBlock{{Name: "x"}}})
	from := strings.Index(out, "FROM ")
	core := strings.Index(out, "byre core block")
	skills := strings.Index(out, "# --- skills ---")
	entry := strings.Index(out, "ENTRYPOINT")
	if from < 0 || core < 0 || skills < 0 || entry < 0 {
		t.Fatalf("missing required sections:\n%s", out)
	}
	// Infra (constant) must precede skills (so the dev user + gosu exist for
	// skill builds, and the core block stays cache-shared); ENTRYPOINT last.
	if !(from < core && core < skills && skills < entry) {
		t.Fatalf("sections out of order: FROM=%d core=%d skills=%d ENTRYPOINT=%d\n%s", from, core, skills, entry, out)
	}
}

// TestDockerfileLastHealthcheckIsNone pins the security property behind the
// tail HEALTHCHECK strip (the chassis' sole copy): the last HEALTHCHECK in a
// Dockerfile wins, so a raw block (skill Dockerfile lines, project
// dockerfile_post) reintroducing one must still lose to the tail instruction
// — otherwise a base-image-style probe reopens the pre-launch-gate network
// window.
func TestDockerfileLastHealthcheckIsNone(t *testing.T) {
	out := Dockerfile(Input{
		Base: "node:22",
		Skills: []SkillBlock{{
			Name:       "svc",
			Dockerfile: []string{`HEALTHCHECK CMD curl -f http://localhost/ || exit 1`},
		}},
		DockerfilePost: []string{`HEALTHCHECK --interval=5s CMD wget -q http://localhost/`},
	})
	last := strings.LastIndex(out, "HEALTHCHECK")
	if last < 0 {
		t.Fatalf("no HEALTHCHECK emitted:\n%s", out)
	}
	if !strings.HasPrefix(out[last:], "HEALTHCHECK NONE\n") {
		t.Fatalf("last HEALTHCHECK is not NONE (a raw block can reopen the pre-gate window):\n%s", out)
	}
}

// TestDockerfileSkillAptHoistsAboveSkillBlocks pins the ADR 0042 property: a
// LATER skill's apt RUN precedes an EARLIER skill's COPYs and raw lines, so
// payload/raw-line churn in any skill cannot invalidate any apt layer.
func TestDockerfileSkillAptHoistsAboveSkillBlocks(t *testing.T) {
	out := Dockerfile(Input{Base: "node:22", Skills: []SkillBlock{
		{
			Name:       "early",
			Files:      map[string]string{"skills/early/tool.sh": "/usr/local/bin/early-tool"},
			Dockerfile: []string{"RUN /usr/local/bin/early-tool --install"},
		},
		{Name: "late", Apt: []string{"ripgrep"}},
	}})
	apt := strings.Index(out, "--no-install-recommends 'ripgrep'")
	copyLine := strings.Index(out, CopyLine("skills/early/tool.sh", "/usr/local/bin/early-tool"))
	raw := strings.Index(out, "RUN /usr/local/bin/early-tool --install")
	if apt < 0 || copyLine < 0 || raw < 0 {
		t.Fatalf("missing apt/copy/raw line:\napt=%d copy=%d raw=%d\n%s", apt, copyLine, raw, out)
	}
	if !(apt < copyLine && apt < raw) {
		t.Fatalf("later skill's apt should precede earlier skill's COPY and raw lines: apt=%d copy=%d raw=%d\n%s", apt, copyLine, raw, out)
	}
	// The hoisted section carries per-skill attribution, and skills without
	// apt contribute no comment line to it.
	hoisted := strings.Index(out, "# --- skill apt (hoisted")
	blocks := strings.Index(out, "# --- skills ---")
	if !(hoisted >= 0 && hoisted < blocks) {
		t.Fatalf("hoisted apt section missing or after skill blocks: hoisted=%d blocks=%d\n%s", hoisted, blocks, out)
	}
	section := out[hoisted:blocks]
	if !strings.Contains(section, "# skill: late\n") || strings.Contains(section, "# skill: early\n") {
		t.Fatalf("hoisted section attribution wrong (want late only):\n%s", section)
	}
}

func TestDockerfileSkillFilesCopy(t *testing.T) {
	out := Dockerfile(Input{Base: "node:22", Skills: []SkillBlock{{
		Name:       "tools",
		Files:      map[string]string{"skills/tools/review.sh": "/usr/local/bin/byre-review"},
		Dockerfile: []string{"RUN chmod +x /usr/local/bin/byre-review"},
	}}})
	copyLine := CopyLine("skills/tools/review.sh", "/usr/local/bin/byre-review")
	ci := strings.Index(out, copyLine)
	ri := strings.Index(out, "RUN chmod +x /usr/local/bin/byre-review")
	if ci < 0 {
		t.Fatalf("skill file COPY missing:\n%s", out)
	}
	// COPY must precede the raw RUN that uses the file.
	if !(ci < ri) {
		t.Fatalf("COPY should precede RUN that uses it: copy=%d run=%d\n%s", ci, ri, out)
	}
}

// TestDockerfileGuardReassertsAfterProjectFiles pins the security guard: the
// launcher (always) and any Guard files (a posture skill's gate + netns script)
// are re-COPY'd AFTER the project block, so a project `files` clobber of those
// paths loses to byre's own copy. Without this a one-line `files` entry could
// empty the launch gate or stub the firewall while status still reads
// deny-by-default. Regression pin for the launch-gate clobber (2026-07-19).
func TestDockerfileGuardReassertsAfterProjectFiles(t *testing.T) {
	out := Dockerfile(Input{
		Base: "node:22",
		Skills: []SkillBlock{{
			Name: "firewall",
			Files: map[string]string{
				"skills/firewall/firewall.sh": "/usr/local/bin/byre-firewall",
				"skills/firewall/launch-gate": LaunchGatePath,
			},
		}},
		// A hostile project `files` clobber of every security path.
		Files: map[string]string{
			"files/evil-launch": LauncherPath,
			"files/evil-fw":     "/usr/local/bin/byre-firewall",
			"files/empty-gate":  LaunchGatePath,
		},
		Guard: []GuardFile{
			{Staged: "skills/firewall/launch-gate", Dest: LaunchGatePath, Exec: false},
			{Staged: "skills/firewall/firewall.sh", Dest: "/usr/local/bin/byre-firewall", Exec: true},
		},
	})
	// Each project clobber COPY must come BEFORE byre's guard re-COPY, so byre's
	// copy is the last write to the path and wins in the built image.
	for _, tc := range []struct {
		clobber string
		guard   string
	}{
		{CopyLine("files/evil-launch", LauncherPath), "COPY byre-launch " + LauncherPath},
		{CopyLine("files/evil-fw", "/usr/local/bin/byre-firewall"), CopyLine("skills/firewall/firewall.sh", "/usr/local/bin/byre-firewall")},
		{CopyLine("files/empty-gate", LaunchGatePath), CopyLine("skills/firewall/launch-gate", LaunchGatePath)},
	} {
		ci := strings.Index(out, tc.clobber)
		gi := strings.LastIndex(out, tc.guard)
		if ci < 0 || gi < 0 {
			t.Fatalf("missing clobber or guard line:\nclobber %q (%d)\nguard %q (%d)\n%s", tc.clobber, ci, tc.guard, gi, out)
		}
		if !(ci < gi) {
			t.Fatalf("guard must re-COPY after the project clobber: clobber=%d guard=%d\n%s", ci, gi, out)
		}
	}
	// The guard is the LAST thing before the USER/ENTRYPOINT tail (after
	// HEALTHCHECK NONE nothing re-COPYs the paths).
	guard := strings.Index(out, "byre security guard")
	entry := strings.Index(out, "ENTRYPOINT")
	if !(guard > 0 && guard < entry) {
		t.Fatalf("guard not positioned before ENTRYPOINT: guard=%d entry=%d\n%s", guard, entry, out)
	}
	// The netns script re-COPY carries its chmod +x (a staged script is 0644).
	if !strings.Contains(out, "RUN chmod +x '/usr/local/bin/byre-firewall'") {
		t.Fatalf("guard should re-chmod the netns script:\n%s", out)
	}
}

